def get_region(region_code: str) -> RegionInfo | None:
    """地域コードから地域情報を返す。存在しない場合は None。"""
    return REGIONS.get(region_code)


def get_regions_by_prefecture(prefecture_code: str) -> dict[str, RegionInfo]:
    """都道府県コード（2桁）に一致する地域をすべて返す。"""
    return {
        code: info
        for code, info in REGIONS.items()
        if code.startswith(f"{prefecture_code}_")
    }


def list_all_prefecture_codes() -> list[str]:
    """登録されている都道府県コードの一覧を返す（重複なし・昇順）。"""
    seen: set[str] = set()
    result: list[str] = []
    for code in REGIONS:
        pref = code.split("_")[0]
        if pref not in seen:
            seen.add(pref)
            result.append(pref)
    return sorted(result)
